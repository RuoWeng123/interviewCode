# 绝对值
print(abs(-1))  # 输出：1

# 向上取整
print(math.ceil(1.1))  # 输出：2

# 向下取整
print(math.floor(1.9))  # 输出：1

# 求和
print(sum([1, 2, 3]))  # 输出：6

# 求最大值
print(max([1, 2, 3]))  # 输出：3

# 求最小值
print(min([1, 2, 3]))  # 输出：1

# 精度过滤
print(round(1.23456, 2))  # 输出：1.23

# 四舍五入
print(round(1.5))  # 输出：2

# 幂
print(pow(2, 3))  # 输出：8

# 开方
print(math.sqrt(4))  # 输出：2.0

# 随机数
print(random.random())  # 输出：0.4288890546751146

# 随机整数
print(random.randint(1, 10))  # 输出：7

# 截断
print(math.trunc(1.9))  # 输出：1

# 判断 NaN
print(math.isnan(1))  # 输出：False

# 判断正无穷
print(math.isinf(1))  # 输出：False

# 转型
print(int('1'))  # 输出：1

# 转型
print(float('1.1'))  # 输出：1.1

# 转型
print(str(1))  # 输出：'1'